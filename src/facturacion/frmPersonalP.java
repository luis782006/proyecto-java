    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtLegajoP.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero de legajo");
        } else {
            N_Legajo = txtLegajoP.getText();
            if (txtNombreP.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese el nombre de la persona");
            } else {
                Nombre = txtNombreP.getText();
                if (txtApellidoP.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Ingrese el apellido de la persona");
                } else {
                    Apellido = txtApellidoP.getText();
                    if (cmbCargoP.getSelectedItem() == null) {
                        JOptionPane.showMessageDialog(null, "Escoja una categoria");

                    } else {
                        Cargo = String.valueOf(cmbCargoP.getSelectedItem());
                        if (txtTelefonoP.getText().equals("")) {
                            JOptionPane.showMessageDialog(null, "Ingrese un numero de telefono");
                        } else {

                            Telefono = txtTelefonoP.getText();
                            Salario=txtSalarioP.getText();
                            try {
                                getFecha();
                                mi_Fecha = Fecha;
                                BorrarTxt();
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Ingrese la fecha de Alta");
                            }
                        }
                    }
                }
            }
            String nl = System.getProperty("line.separator");// variable separadora de linea
            JOptionPane.showMessageDialog(null, "Ingreso"
                    + nl + "Numero de Legajo: " + N_Legajo
                    + nl + "Nombre: " + Nombre
                    + nl + "Apellido: " + Apellido
                    + nl + "Cargo: " + Cargo
                    + nl + "Telefono: " + Telefono
                    + nl + "Fecha de Ingreso: " + mi_Fecha
                    + nl + "Salario: " + Salario);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

   
