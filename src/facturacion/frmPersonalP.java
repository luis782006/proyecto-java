    public void getFecha(){
     //obtengo la fecha de dtFecha    
     String dia = Integer.toString(dtCalendario.getCalendar().get(java.util.Calendar.DAY_OF_MONTH));
     String año = Integer.toString(dtCalendario.getCalendar().get(java.util.Calendar.YEAR));
     String mes = Integer.toString(dtCalendario.getCalendar().get(java.util.Calendar.MONTH) +1);
     //le paso el valor a campo de texto
     Fecha=dia+"/"+mes+"/"+año; 
            
    }

